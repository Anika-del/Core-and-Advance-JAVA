public class ToLowerCase {
//convert into upper and lower case
    public static void main(String[] args) {
        
        String Str = new String("Welcome to Tutorials of String"); 

        System.out.print("Return Value : ");
        System.out.println(Str.toUpperCase()); //WELCOME TO TUTORIALS OF STRING
        System.out.println(Str.toLowerCase());  //welcome to tutorials of string
    }
    
}
